#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	*ptr;

	ptr = s;
	while (*ptr != '\0')
	{
		write(fd, ptr, 1);
		ptr++;
	}
	write(fd, "\n", 1);
}
